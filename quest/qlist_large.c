//Find the First Index to have exp level >= x
int Find(int x)
{
	int total;
	int low,high,now;
	total=sizeof(quest);

	if(quest[0]["exp_bonus"]>=x)
		return 0;
	if(quest[total-1]["exp_bonus"]<x)
		return total-1;

	low=0;
	high=total-1;
	now=(low+high)/2;

	while(high-low>1) {
		if(quest[now]["exp_bonus"]>=x)
			high=now;
		else 
   			low=now;
		now=(low+high)/2;
	}
	return high;
}

int list_quest(int exp) {

	int a, b, c, low, high, i, j;
	mapping list;
	exp = exp/1000;
		
	if (exp > 7500) {		// (7.5M+)  [-800k, +500k];
		low = exp - 800;
		high = exp + 500;	
	} else if (exp > 5000) {	// (5M- 7.5M) [-500k, +400k]	
		low = exp - 400;
		high = exp + 300;	
	} else if(exp>1000) {		// (1M - 5M) [-400k, + 300k]
		low = exp - 300; 
		high = exp + 200;
	} else if (exp>750) {		//  (750k-1M) [0.8,1.2] 
		low=exp*8/10;
		high=exp*11/10;
	} else if (exp > 500) {		//  (500k-750k) [0.7,1.3]
		low=exp*7/10;
		high=exp*11/10;
	}
	else {				//  (0-500k) [0.6,1.4]
		low = exp * 6/10;
		high = exp *11/10;
	} 
	
	if( low>12000) low=12000;  

	if(high<3) high=3;
	a=Find(low);
	b=Find(high)-1;	
		
	CHANNEL_D->do_sys_channel("sys"," low == "+ low + "  high = " + high + " \n");
	
	for (i=a;i<b;i++){
			j++;
			CHANNEL_D->do_sys_channel("sys", sprintf("j=%d %s %s %s\t",
					j,
					quest[i]["quest_location"],
					quest[i]["quest_type"],
					quest[i]["quest"]
					));
	}
	return 1;
		
}

